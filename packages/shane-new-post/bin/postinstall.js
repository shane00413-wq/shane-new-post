#!/usr/bin/env node

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { input } from "@inquirer/prompts";
import { askLangIfNeeded, makeT, packageDirFromImportMeta } from "../lib/i18n.js";

// npm/pnpm sets INIT_CWD to the directory the user actually ran the install
// command from, not the node_modules directory the postinstall script lives in
const projectRoot = process.env.INIT_CWD || process.cwd();
const pkgJsonPath = path.join(projectRoot, "package.json");

// This package's own directory (node_modules/xxx/), used to locate the template script
const packageDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const templatePath = path.join(packageDir, "bin", "new-post.js");

// Filenames of a same-purpose script that might already exist in the target
// scripts directory (the user's own, or from an older version). If any of
// these match, that filename is reused/overwritten instead of creating a new one
const CANDIDATE_NAMES = [
  "new-post.js",
  "new post.js",
  "new-blog.js",
  "new blog.js",
  "newpost.js",
  "newblog.js",
  "new posts.js",
  "new-posts.js",
  "newposts.js",
  "new blogs.js",
  "new-blogs.js",
  "newblogs.js",
];

// Marker written at the top of the generated file, used to tell whether this
// file has already been taken over by this package. Only the first
// encounter (file missing, or present without the marker) triggers
// generate+ask+overwrite; a file that already has the marker is left alone
// on later reinstalls, protecting the user's manual edits.
const MANAGED_MARKER = "// @generated by shane-new-post";
const CONFIG_FILE = ".shane-new-post.json";

function readProjectPackage() {
  if (!fs.existsSync(pkgJsonPath)) {
    return null;
  }

  try {
    return JSON.parse(fs.readFileSync(pkgJsonPath, "utf8"));
  } catch {
    return null;
  }
}

function readExistingConfig() {
  const configPath = path.join(projectRoot, CONFIG_FILE);
  if (!fs.existsSync(configPath)) return null;
  try {
    return JSON.parse(fs.readFileSync(configPath, "utf8"));
  } catch {
    return null;
  }
}

function isOwnRepository(pkg) {
  return pkg?.name === "shane-new-post";
}

function isAstroProject(pkg) {
  if (!pkg) return false;

  const deps = {
    ...(pkg.dependencies || {}),
    ...(pkg.devDependencies || {}),
    ...(pkg.optionalDependencies || {}),
  };

  return Boolean(deps.astro);
}

// Checks the scripts directory for an existing same-purpose script, returns
// the matched filename, or null if none found
function findExistingScript(scriptsDir) {
  for (const name of CANDIDATE_NAMES) {
    if (fs.existsSync(path.join(scriptsDir, name))) {
      return name;
    }
  }

  return null;
}

// Checks whether an existing file was previously generated by this package (carries the marker)
function isAlreadyManaged(filePath) {
  if (!fs.existsSync(filePath)) {
    return false;
  }

  try {
    const firstLines = fs.readFileSync(filePath, "utf8").slice(0, 200);
    return firstLines.includes(MANAGED_MARKER);
  } catch {
    return false;
  }
}


function writeConfig(author, lang) {
  const configPath = path.join(projectRoot, CONFIG_FILE);

  if (fs.existsSync(configPath)) {
    return;
  }

  const config = {
    author,
    lang,
  };

  fs.writeFileSync(configPath, JSON.stringify(config, null, 2) + "\n", "utf8");
  console.log(t("configCreated", { file: CONFIG_FILE }));
}

function writeScript(scriptsDir) {

  if (!fs.existsSync(scriptsDir)) {
    fs.mkdirSync(scriptsDir, { recursive: true });
    console.log(t("createdDir", { dir: path.relative(projectRoot, scriptsDir) }));
  }

  const existingName = findExistingScript(scriptsDir);
  const targetName = existingName || "new-post.js";
  const targetPath = path.join(scriptsDir, targetName);

  // Already taken over by this package, don't overwrite/ask again, protecting manual edits
  if (isAlreadyManaged(targetPath)) {
    console.log(t("alreadyManagedSkip", { path: path.relative(projectRoot, targetPath) }));
    return { targetPath, wrote: false };
  }

  const overwriting = fs.existsSync(targetPath);

  // Before taking over a file that wasn't generated by this package, back it up as .bak first, just in case
  if (overwriting) {
    const backupPath = targetPath + ".bak";
    fs.copyFileSync(targetPath, backupPath);
    console.log(t("backedUp", { path: path.relative(projectRoot, backupPath) }));
  }

  const rawTemplate = fs.readFileSync(templatePath, "utf8");

  // The marker has to go right after the shebang (#!...), not on the very
  // first line, or the script won't be directly executable
  let content;
  if (rawTemplate.startsWith("#!")) {
    const newlineIndex = rawTemplate.indexOf("\n");
    content =
      rawTemplate.slice(0, newlineIndex + 1) +
      MANAGED_MARKER + "\n" +
      rawTemplate.slice(newlineIndex + 1);
  } else {
    content = MANAGED_MARKER + "\n" + rawTemplate;
  }

  // Note: the author name isn't hardcoded into this script -- the template
  // reads it at runtime from .shane-new-post.json in the project root (see
  // writeConfig), so we just write the template content as-is here, no
  // constant substitution needed (or wanted).

  fs.writeFileSync(targetPath, content, "utf8");

  console.log(
    overwriting
      ? t("tookOver", { path: path.relative(projectRoot, targetPath) })
      : t("generated", { path: path.relative(projectRoot, targetPath) })
  );

  return { targetPath, wrote: true };
}

function injectPackageScript(pkg, scriptPath) {
  pkg.scripts ??= {};

  const relative = "node " + path.relative(projectRoot, scriptPath).replace(/\\/g, "/");

  if (pkg.scripts["new-post"] === relative) {
    return;
  }

  pkg.scripts["new-post"] = relative;

  fs.writeFileSync(pkgJsonPath, JSON.stringify(pkg, null, 2) + "\n", "utf8");

  console.log(t("scriptInjected", { cmd: relative }));
}

async function askAuthor() {

  if (!process.stdin.isTTY) {
    return "";
  }

  try {
    return await input({
      message: t("askAuthor"),
      default: "",
    });
  } catch {
    return "";
  }
}

let t;

async function main() {

  const pkg = readProjectPackage();

  if (!pkg) {
    process.exit(0);
  }

  const existingConfig = readExistingConfig();
  const lang = await askLangIfNeeded({ config: existingConfig });
  t = makeT(packageDirFromImportMeta(import.meta.url), lang);

  if (isOwnRepository(pkg)) {
    console.log(t("ownRepoSkip"));
    process.exit(0);
  }

  if (!isAstroProject(pkg)) {
    console.log(t("notAstro"));
    process.exit(0);
  }

  const scriptsDir = path.join(projectRoot, "scripts");

  // First check whether the target file is already managed by this package -- skip entirely if so
  const existingName = fs.existsSync(scriptsDir) ? findExistingScript(scriptsDir) : null;
  const targetName = existingName || "new-post.js";
  const targetPath = path.join(scriptsDir, targetName);

  if (isAlreadyManaged(targetPath)) {
    console.log(t("alreadyManagedSkip", { path: path.relative(projectRoot, targetPath) }));
    injectPackageScript(pkg, targetPath);
    process.exit(0);
  }

  const author = await askAuthor();

  const { targetPath: writtenPath } = writeScript(scriptsDir);
  writeConfig(author, lang);

  injectPackageScript(pkg, writtenPath);
}

main();
