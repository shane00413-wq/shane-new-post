#!/usr/bin/env node

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { select, input } from "@inquirer/prompts";

// npm/pnpm 安装依赖时会把 INIT_CWD 设为「用户实际执行安装命令」的目录
// 而不是 postinstall 脚本自身所在的 node_modules 目录
const projectRoot = process.env.INIT_CWD || process.cwd();
const pkgJsonPath = path.join(projectRoot, "package.json");

// 本包自身的目录（node_modules/xxx/），用来找到模板脚本原文件
const packageDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const templatePath = path.join(packageDir, "bin", "new-post.js");

// 目标 scripts 目录里，可能已经存在的「用户自己/旧版本」同用途脚本文件名
// 只要匹配到其中任意一个，就复用这个文件名覆盖写入，而不是另外新建一份
const CANDIDATE_NAMES = [
  "new-post.js",
  "new post.js",
  "new-blog.js",
  "new blog.js",
];

// 写在生成文件顶部的标记，用来判断这个文件「是不是本包已经接管过的文件」
// 只有第一次遇到（不存在 / 存在但没有这个标记）才会生成+询问+覆盖
// 已经带有这个标记的文件，后续重装依赖不会再动它，保护用户之后的手动修改
const MANAGED_MARKER = "// @generated by shane-new-post";
const CONFIG_FILE = ".shane-new-post.json";

function readProjectPackage() {
  if (!fs.existsSync(pkgJsonPath)) {
    return null;
  }

  try {
    return JSON.parse(fs.readFileSync(pkgJsonPath, "utf8"));
  } catch {
    return null;
  }
}

function isOwnRepository(pkg) {
  return pkg?.name === "shane-new-post";
}

function isAstroProject(pkg) {
  if (!pkg) return false;

  const deps = {
    ...(pkg.dependencies || {}),
    ...(pkg.devDependencies || {}),
    ...(pkg.optionalDependencies || {}),
  };

  return Boolean(deps.astro);
}

// 找 scripts 目录下是否已经有同用途脚本，返回匹配到的文件名；没有就返回 null
function findExistingScript(scriptsDir) {
  for (const name of CANDIDATE_NAMES) {
    if (fs.existsSync(path.join(scriptsDir, name))) {
      return name;
    }
  }

  return null;
}

// 判断某个已存在的文件是不是本包之前生成过的（带标记）
function isAlreadyManaged(filePath) {
  if (!fs.existsSync(filePath)) {
    return false;
  }

  try {
    const firstLines = fs.readFileSync(filePath, "utf8").slice(0, 200);
    return firstLines.includes(MANAGED_MARKER);
  } catch {
    return false;
  }
}


function writeConfig(categoryEnabled, author) {
  const configPath = path.join(projectRoot, CONFIG_FILE);

  if (fs.existsSync(configPath)) {
    return;
  }

  const config = {
    category: categoryEnabled,
    author,
  };

  fs.writeFileSync(configPath, JSON.stringify(config, null, 2) + "\\n", "utf8");
  console.log(`✅ 已创建配置文件: ${CONFIG_FILE}`);
}

function writeScript(scriptsDir, categoryEnabled, author) {

  if (!fs.existsSync(scriptsDir)) {
    fs.mkdirSync(scriptsDir, { recursive: true });
    console.log(`✅ 已创建目录: ${path.relative(projectRoot, scriptsDir)}`);
  }

  const existingName = findExistingScript(scriptsDir);
  const targetName = existingName || "new-post.js";
  const targetPath = path.join(scriptsDir, targetName);

  // 已经是本包接管过的文件，不再重复覆盖/询问，保护用户后续的手动修改
  if (isAlreadyManaged(targetPath)) {
    console.log(`ℹ️ ${path.relative(projectRoot, targetPath)} 已存在且由本包管理，跳过`);
    return { targetPath, wrote: false };
  }

  const overwriting = fs.existsSync(targetPath);

  // 接管一个「不是本包生成的」已有文件之前，先备份一份 .bak，多一层保险
  if (overwriting) {
    const backupPath = targetPath + ".bak";
    fs.copyFileSync(targetPath, backupPath);
    console.log(`📦 已备份原文件: ${path.relative(projectRoot, backupPath)}`);
  }

  const rawTemplate = fs.readFileSync(templatePath, "utf8");

  // 标记要插在 shebang（#!...）之后，不能顶到第一行，否则脚本没法直接执行
  let content;
  if (rawTemplate.startsWith("#!")) {
    const newlineIndex = rawTemplate.indexOf("\n");
    content =
      rawTemplate.slice(0, newlineIndex + 1) +
      MANAGED_MARKER + "\n" +
      rawTemplate.slice(newlineIndex + 1);
  } else {
    content = MANAGED_MARKER + "\n" + rawTemplate;
  }

  // 把模板里的开关常量替换成安装时选择/填写的结果
  content = content.replace(
    /const CATEGORY_ENABLED = (true|false);/,
    `const CATEGORY_ENABLED = ${categoryEnabled};`
  );

  content = content.replace(
    /const AUTHOR = "[^"]*";/,
    `const AUTHOR = ${JSON.stringify(author)};`
  );

  fs.writeFileSync(targetPath, content, "utf8");

  console.log(
    overwriting
      ? `✅ 检测到已有同用途脚本，已接管并覆盖: ${path.relative(projectRoot, targetPath)}`
      : `✅ 已生成脚本: ${path.relative(projectRoot, targetPath)}`
  );

  return { targetPath, wrote: true };
}

function injectPackageScript(pkg, scriptPath) {
  pkg.scripts ??= {};

  const relative = "node " + path.relative(projectRoot, scriptPath).replace(/\\/g, "/");

  if (pkg.scripts["new-post"] === relative) {
    return;
  }

  pkg.scripts["new-post"] = relative;

  fs.writeFileSync(pkgJsonPath, JSON.stringify(pkg, null, 2) + "\n", "utf8");

  console.log(`✅ 已在 package.json 写入 scripts.new-post = "${relative}"`);
}

async function askCategoryEnabled() {

  // 非交互环境（CI 等）没有 TTY，直接用默认值 false，不弹选项
  if (!process.stdin.isTTY) {
    return false;
  }

  try {
    return await select({
      message: "是否启用分类快捷创建",
      choices: [
        { name: "no", value: false },
        { name: "yes", value: true },
      ],
      default: false,
    });
  } catch {
    // 用户 Ctrl+C 或非交互场景，安全兜底为 false
    return false;
  }
}

async function askAuthor() {

  if (!process.stdin.isTTY) {
    return "";
  }

  try {
    return await input({
      message: "作者名(直接回车不写 author 字段):",
      default: "",
    });
  } catch {
    return "";
  }
}

async function main() {

  const pkg = readProjectPackage();

  if (!pkg) {
    process.exit(0);
  }

  if (isOwnRepository(pkg)) {
    console.log("ℹ️ 检测到开发仓库，跳过 postinstall");
    process.exit(0);
  }

  if (!isAstroProject(pkg)) {
    console.log("ℹ️ 当前项目未检测到 astro 依赖，跳过脚本安装");
    process.exit(0);
  }

  const scriptsDir = path.join(projectRoot, "scripts");

  // 先看看目标文件是不是已经被本包接管过，是的话完全跳过（包括不问分类）
  const existingName = fs.existsSync(scriptsDir) ? findExistingScript(scriptsDir) : null;
  const targetName = existingName || "new-post.js";
  const targetPath = path.join(scriptsDir, targetName);

  if (isAlreadyManaged(targetPath)) {
    console.log(`ℹ️ ${path.relative(projectRoot, targetPath)} 已存在且由本包管理，跳过`);
    injectPackageScript(pkg, targetPath);
    process.exit(0);
  }

  const categoryEnabled = await askCategoryEnabled();
  const author = await askAuthor();

  const { targetPath: writtenPath } = writeScript(scriptsDir, categoryEnabled, author);
  writeConfig(categoryEnabled, author);

  injectPackageScript(pkg, writtenPath);

  console.log(
    categoryEnabled
      ? "✅ 已启用分类快捷创建"
      : "ℹ️ 未启用分类快捷创建"
  );
}

main();
